'''Знайти добуток всіх елементів масиву цілих чисел, менших 0.
Розмірність масиву - 10. Заповнення масиву здійснити за допомогою клавіатури. '''
n = 10
array = [int(input(f'Введіть {i} елемент масиву: ')) for i in range(n)]
print('Даний масив:\n', array)
dob = 1
for j in array:
    if j < 0:
        dob *= j
if dob > 1:
    print(f'Добуток усіх елементів масиву цілих чисел, менших 0 дорівнює {dob}')
else:
    print('Таких елементів немає')