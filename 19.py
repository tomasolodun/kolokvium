'''Знайти суму всіх елементів масиву цілих чисел що задовольняють умові: остача від ділення на 2 дорівнює 3.
 Розмірність масиву - 20. Заповнення масиву здійснити випадковими числами від 200 до 300.'''
import random

array = [random.randint(200, 300) for i in range(20)]
print('Даний масив:\n', array)
sum = 0
for i in array:
    if i % 2 == 3:
        sum += i
print(f'Сума всіх елементів масиву цілих чисел, що задовольняють умові: остача від ділення на 2 дорівнює 3 дорівнює {sum}')