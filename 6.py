'''Створіть масив А [1..8] за допомогою генератора випадкових чисел з елементами від -10 до 10 і виведіть його на екран.
Підрахуйте кількість від’ємних елементів масиву. '''
import random
array = [random.randint(-10, 10) for i in range(8)]
print('Даний масив:\n', array)
counter = 0
for j in array:
    if j < 0:
        counter += 1
print(f"У даному масиві {counter} вд'ємних елементів")