'''Знайти суму додатніх елементів лінійного масиву цілих чисел. Розмірність масиву - 10. Заповнення масиву здійснити з клавіатури. '''
array = [int(input(f'Введіть {i} елемент масиву: ')) for i in range(10)]
print('Даний масив:\n', array)
sum = 0
for i in array:
    if i > 0:
        sum += i
print('Сума додатніх елементів лінійного масиву цілих чисел = ',sum)