'''Знайти суму парних елементів масиву цілих чисел. Розмірність масиву - 20.
Заповнення масиву здійснити випадковими числами від 100 до 200.'''
import random

array = [random.randint(100, 200) for i in range(20)]
print('Даний масив:\n', array)
sum = 0
for j in array:
    if j % 2 == 0:
        sum += j
print(f'Сума парних елементів масиву дорівнює {sum}')
