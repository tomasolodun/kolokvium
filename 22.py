'''Знайти добуток елементів масиву, кратних 3 і 9. Розмірність масиву - 10.
Заповнення масиву здійснити випадковими числами від 5 до 500. '''
import random

array = [random.randint(5, 500) for i in range(10)]
print('Даний масив:\n', array)
dob = 1
for j in array:
    if j % 3 == 0 and j % 9 == 0:
        dob *= j
if dob > 1:
    print(f'Добуток елементів масиву, кратних 3 і 9 дорівнює {dob}')
else:
    print('Таких елементів немає')