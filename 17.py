'''Знайти суму елементів масиву дійсних чисел, що мають непарні номери.
Розмірність масиву - 20. Заповнення масиву здійснити випадковими числами від 100 до 200. '''
import random

array = [random.uniform(100, 200) for i in range(20)]
print('Даний масив:\n', array)
sum = 0
for j in range(0, len(array), 2):
    sum += array[j]
print("Сума елементів, що мають непарні номери: ", sum)
