'''Дані про направлення вітру (північний, південний, східний, західний) і силі вітру за декаду листопада зберігаються в масиві.
Визначити, скільки днів дув південний вітер з силою, що перевищує 8 м / с. '''
import random
windInfo = [[random.randint(1, 4), random.randint(0, 20)] for i in range(10)]
print(windInfo)
counter = 0
for i in windInfo:
    if i[0] == 2 and i[1] > 8:
        counter += 1
print('Кількість днів,коли південний вітер дув з силою, що перевищує 8 м/с :',counter)