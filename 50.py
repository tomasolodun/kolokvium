'''У лотереї розігрувалося 100 квитків. Таблиця містить 10 номерів виграшних квитків. Перевірте, чи є квиток з номером N виграшним. '''
import random
N = int(input("Введіть Ваш номер: "))
happy_tickets = {random.randint(0, 100) for i in range(11)}
print("Щасливі білети:\n", happy_tickets)
if N in happy_tickets:
    print('Ваш білет - щасливий! Вітання!')
else:
    print('На жаль, Ваш білет не є щасливий, спробуйте іншим разом!')